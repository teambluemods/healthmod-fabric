package io.github.blueminecraftteam.healthmod.client.gui.screen

import com.mojang.blaze3d.systems.RenderSystem
import net.minecraft.client.gui.screen.ingame.HandledScreen
import net.minecraft.client.util.math.MatrixStack
import net.minecraft.entity.player.PlayerInventory
import net.minecraft.text.Text
import net.minecraft.util.Identifier

class BloodTestMachineScreen(handler: BloodTestMachineScreenHandler, inventory: PlayerInventory, title: Text) :
    HandledScreen<BloodTestMachineScreenHandler>(handler, inventory, title) {
    override fun drawBackground(matrices: MatrixStack, delta: Float, mouseX: Int, mouseY: Int) {
        RenderSystem.color4f(1.0f, 1.0f, 1.0f, 1.0f)
        client!!.textureManager.bindTexture(TEXTURE)
        val x = (width - backgroundWidth) / 2
        val y = (height - backgroundHeight) / 2
        drawTexture(matrices, x, y, 0, 0, backgroundWidth, backgroundHeight)
    }

    override fun render(matrices: MatrixStack?, mouseX: Int, mouseY: Int, delta: Float) {
        renderBackground(matrices)
        super.render(matrices, mouseX, mouseY, delta)
        drawMouseoverTooltip(matrices, mouseX, mouseY)
    }

    override fun init() {
        super.init()

        titleX = (backgroundWidth - textRenderer.getWidth(title)) / 2
    }

    companion object {
        // TODO: use own texture?
        private val TEXTURE = Identifier("minecraft", "textures/gui/container/brewing_stand.png")
    }
}